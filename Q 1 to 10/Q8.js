// JavaScript Program to Find the Largest Among Three Numbers

{
    let a = 10, b = 25, c = 15;

    if (a >= b && a >= c) {
        console.log(a + " is the largest");
    } else if (b >= a && b >= c) {
        console.log(b + " is the largest");
    } else {
        console.log(c + " is the largest");
    }

}