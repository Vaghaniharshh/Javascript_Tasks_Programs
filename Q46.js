//  JavaScript Program to Create Objects in Different Ways

// Using Object Literal
{
    let person = {
        name: "Harsh",
        age: 21,
        city: "Gujarat"
    };
    console.log(person);
}

// Using new Object
{
    let car = new Object();
    car.brand = "Tesla";
    car.model = "Model S";
    car.year = 2024;

    console.log(car);
}